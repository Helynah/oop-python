# Find the Length of a List

fruits = ["orange", "apple", "banana"]
num_items = len(fruits)
print(num_items)

# Appending Elements to a List

fruits = ["orange", "apple", "banana"]
print(fruits)

# Let's add a kiwi!
fruits.append("kiwi")
print(fruits)

# Removing Elements from a List

fruits = ["orange", "apple", "banana"]
print(fruits)

# Let's remove item 1
del fruits[1]
print(fruits)

# Temporarily Sorting a List

fruits = ["orange", "apple", "banana"]
sorted_fruits = sorted(fruits)
print(fruits)
print(sorted_fruits)